CREATE SEQUENCE SEQ_CLIENTES INCREMENT 1 START 1;
CREATE TABLE TB_CLIENTE
(
	ID int PRIMARY KEY DEFAULT NextVal('SEQ_CLIENTES'),
	NOME VARCHAR(64) NOT NULL,
	TELEFONE INTEGER NOT NULL,
	STATUS VARCHAR(64) NOT NULL,
	LIMITE_CARTAO FLOAT8 NOT NULL
);

CREATE TABLE TB_CIDADES
(
	CIDADE VARCHAR(64) NOT NULL,
	UF VARCHAR(64) NOT NULL,
	BAIRRO VARCHAR(64) NOT NULL,
	id_cd INTEGER REFERENCES TB_CLIENTE(id)
);

CREATE TABLE TB_CEP
(
	CEP INTEGER,
	RUA VARCHAR(64) NOT NULL,
	N_RUA VARCHAR(80) NOT NULL,
	id_cep INTEGER REFERENCES TB_CLIENTE(id)
);

CREATE SEQUENCE SEQ_PRODUTOS INCREMENT 1 START 1;
CREATE TABLE TB_PRODUTO
(
	ID int PRIMARY KEY DEFAULT NextVal('SEQ_PRODUTOS'),
	NOME_PROD VARCHAR(64) NOT NULL,
	CATEGORIA VARCHAR(64) NOT NULL,
	PRECO FLOAT8 NOT NULL
);

CREATE TABLE TB_PEDIDOS
(
	ID_PEDIDO INTEGER REFERENCES TB_PRODUTO(id),
	QUANTIDADE INT,
	DATA date 
);
